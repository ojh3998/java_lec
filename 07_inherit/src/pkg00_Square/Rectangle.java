package pkg00_Square;

public class Rectangle {
  
  // new Rectangle(4,5);
  
  private int width;
  private int height;
  
  public Rectangle(int width, int height) {
   this.width = width;
   this.height = height;
  }
  
  public int getArea() {
    return width * height;
      
    
  
  }
    
    
}
  
    
  

