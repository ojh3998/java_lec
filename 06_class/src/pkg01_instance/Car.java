package pkg01_instance;

// 클래스의 구성요소 (멤버)
// 1. 필드 : 어떤 값을 의미 (변수)
// 2. 메소드 : 기능 (함수)

public class Car {
  
  // 필드 (field), 자동 초기화(0, false, null) -- class 가 가지는 변수를 필드라고함.
  int speed;
  
  // 메소드 (method) -- 반환타입 메소드의 이름 (매개변수) {}
  void drive() {
    System.out.println(speed + "km 속도로 드라이브 중");
  }
  
  
  
  

}
