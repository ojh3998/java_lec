package pkg12_Bakery;

public class BreadNChange {
  
  //field
  private int bread;
  private int change;
  
  //constructor
  public BreadNChange(int bread, int change) {
    
    this.bread = bread;
    this.change = change;
  }
  
  //getter, setter
  public int getBread() {
    return bread;
  }

  public void setBread(int bread) {
    this.bread = bread;
  }

  public int getChange() {
    return change;
  }

  public void setChange(int change) {
    this.change = change;
  }
  

  
  
  
  

}
